    private static long Solve((string, int[])[] lines, int n) =>
        lines.Sum(t => Count(t, n));

    private static long Count((string s, int[] lens) t, int n)
    {
        var s = string.Join('?', Enumerable.Repeat(t.s, n));
        var lens = Enumerable.Repeat(t.lens, n).SelectMany(v => v).ToArray();
        return Count(s, lens, new());
    }

    private static long Count(string s, int[] lens, Dictionary<int, long> counts, int key = 0)
    {
        if (counts.TryGetValue(key, out long count))
            return count;
        if (lens.Length == 0)
            return counts[key] = s.Any(c => c == '#') ? 0 : 1;
        var len = lens[0];
        var max = s.Length - lens.Length - Math.Max(len, lens.Sum() - 1);
        var k = s[..lens[0]].Count(c => c != '.');
        for (int first = 0, last = len; first <= max; )
        {
            char c = s[first++], d = s[last++];
            if (k == len && d != '#')
                count += Count(s[last..], lens[1..], counts, key + last * 32 + 1);
            if (c == '#')
                return counts[key] = count;
            k += (d == '.' ? 0 : 1) - (c == '.' ? 0 : 1);
        }
        if (k == len && lens.Length == 1)
            ++count;
        return counts[key] = count;
    }

    private static (string, int[]) ParseOne(string[] ss) =>
        (ss[0], ss[1].Split(',').Select(int.Parse).ToArray());
    
    static int CheckSpring(string orig_springs, List<int> orig_springnums)
    {
        var combinations = 0;

        var springs = orig_springs;     
        var springnums = orig_springnums.ToList();

        var springnums_length = springnums.Sum() + springnums.Count-1;
        if (springs.Length < springnums_length) return 0;

        for (var i = 0; i<springs.Length; i++)
        {
            if (springs[i] == '#')
            {
                string nextstring;
                if (i+springnums[0] > springs.Length) return 0; 
                var nextset = springs.Substring(i,springnums[0]);
                if (nextset.Contains(".")) return 0;
                if (i+springnums[0] + 1 <= springs.Length) 
                {
                    if (springs[i+springnums[0]] == '#') return 0; 
                    nextstring = springs.Substring(i+springnums[0]+1);
                }
                else nextstring = "";
                springnums.RemoveAt(0);

                if (springnums.Count == 0)
                {
                    if (nextstring.Contains("#")) return 0;
                    else return 1;
                }

                return CheckSpring(nextstring,springnums);
            } 
            if (springs[i] == '?')
            {
                string nextstring;
                if (i+springnums[0] > springs.Length) nextstring = "";
                else nextstring = springs.Substring(i+1);
                return CheckSpring($"#{nextstring}",springnums) + CheckSpring($".{nextstring}",springnums);
            }
        }

        return combinations;
    }
    
